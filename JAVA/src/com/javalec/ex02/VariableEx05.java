package com.javalec.ex02;

public class VariableEx05 {

	public static void main(String[] args) {
		double d = 100;
		int i = 100;
		int result = i + (int)d;
		
		System.out.println("d : " + d); // d: 100:0
		System.out.println("i: " + i); // i: 100
		System.out.println("result : " + result); //result: 200
	}

}
