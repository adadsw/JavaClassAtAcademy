package com.javalec.ex02;

public class VariableEx02 {
	
	public static void main(String[] args) {

		char c = '씨';
		int i = 10;
		double d = 3.14D;
		boolean b = true;
		String s = "오늘 수고 많았어";
		
		System.out.println("c : " + c);
		System.out.println("i : " + i);
		System.out.println("d : " + d);
		System.out.println("b : " + b);
		System.out.println("s : " + s);
		
	}
}