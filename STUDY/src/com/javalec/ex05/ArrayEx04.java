package com.javalec.ex05;

public class ArrayEx04 {
	public static void main(String[] args) {
		int[][] score = {
				{100, 100, 100},
				{20, 20, 20},
				{30, 30, 30},
				{40, 40, 40},
				{50, 50, 50}
		};
		System.out.println("score[0][0] = " + score[0][0]);
		System.out.println("score[0][1] = " + score[0][1]);
		System.out.println("score[0][2] = " + score[0][2]);
		System.out.println("score[1][0] = " + score[1][0]);
		System.out.println("score[1][1] = " + score[1][1]);
		System.out.println("score[1][2] = " + score[1][2]);
		System.out.println("score[2][0] = " + score[2][0]);
		System.out.println("score[2][1] = " + score[2][1]);
		System.out.println("score[2][2] = " + score[2][2]);
		System.out.println("score[3][0] = " + score[3][0]);
		System.out.println("score[3][1] = " + score[3][1]);
		System.out.println("score[3][2] = " + score[3][2]);
		System.out.println("score[4][0] = " + score[4][0]);
		System.out.println("score[4][1] = " + score[4][1]);
		System.out.println("score[4][2] = " + score[4][2]);
	}

}
